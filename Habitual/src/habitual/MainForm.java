    /**
     * This button event is to add new habits to the table and textfile
     *
     * @param evt
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtHabit.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "No text was entered. Please try again.");
        } else {
            try {
                FileOutputStream f = new FileOutputStream("task.txt", true);
                PrintStream p = new PrintStream(f);
                String task = txtHabit.getText();
                p.println(task);
                JOptionPane.showMessageDialog(null, "New Habit added!");
                populateTable();
                p.close();
                f.close();

                txtHabit.setText(null);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    /**
     * This button click event to remove a habit from the table and the textfile
     *
     * @param evt
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int option = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this habit?", "Confirmation", JOptionPane.YES_NO_OPTION);

        if (option == JOptionPane.YES_OPTION) {
            int index = tblHabit.getSelectedRow();
            if (index != -1) {
                String task = (String) tblHabit.getValueAt(index, 0);
                try {
                    FileOutputStream f = new FileOutputStream("temp.txt");
                    PrintStream p = new PrintStream(f);
                    FileInputStream ff = new FileInputStream("task.txt");
                    Scanner input = new Scanner(ff);

                    /**
                     * Takes all data that isn't equal to the entered text and
                     * adds it to the temp textfile
                     */
                    while (true) {
                        try {
                            String s = input.nextLine();
                            if (!task.equalsIgnoreCase(s)) {
                                p.println(s);
                            }
                        } catch (Exception e) {
                            break;
                        }
                    }
                    p.close();
                    f.close();
                    ff.close();
                    input.close();

                    /**
                     * Delete the original textfile with the old data
                     */
                    File file = new File("task.txt");
                    file.delete();

                    /**
                     * Rename the temp file to original
                     */
                    File tempFile = new File("temp.txt");
                    File newFile = new File("task.txt");
                    tempFile.renameTo(newFile);

                    JOptionPane.showMessageDialog(null, "Habit was deleted!");

                    populateTable();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }

            } else {
                JOptionPane.showMessageDialog(null, "No habit was selected. Please select a habit and try again.");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

