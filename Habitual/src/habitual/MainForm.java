    /**
     * Reads text from a textfile(task.txt) and uses it to populate the JTable
     * in the program
     */
    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblHabit.getModel();
        int count = dtm.getRowCount();

        /**
         * Clears the table
         */
        while (count-- != 0) {
            dtm.removeRow(0);
        }

        /**
         * Populating the table with data from the textfile
         */
        try {
            FileInputStream f = new FileInputStream("task.txt");
            Scanner input = new Scanner(f);
            while (true) {
                try {
                    Vector row = new Vector();
                    row.add(input.nextLine());
                    dtm.addRow(row);
                } catch (Exception e) {
                    break;
                }
            }
            f.close();
            input.close();
        } catch (Exception e) {
            
        }
    }

