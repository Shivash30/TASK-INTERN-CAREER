    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:

        if ((rbtnStudent.isSelected()) || (rbtnTeacher.isSelected())) {
            String email = txtEmail.getText();
            String password = txtPassword.getText();

            if (rbtnStudent.isSelected()) {
                if (verifyLoginStudent(email, password)) {
                    JOptionPane.showMessageDialog(null, "Login Successful!");
                    new stu_Menu(studentNumber).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Login Unsuccessful!");
                }
            }

            if (rbtnTeacher.isSelected()) {
                if (verifyLoginTeacher(email, password)) {
                    JOptionPane.showMessageDialog(null, "Login Successful!");
                    new tch_Menu().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Login Unsuccessful!");
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "User wasn't selected!");
        }

        clearContents();
    }//GEN-LAST:event_btnLoginActionPerformed

