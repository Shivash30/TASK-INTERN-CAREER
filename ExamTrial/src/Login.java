    public boolean verifyLoginStudent(String email, String password) {
        try (java.sql.Connection con = new ConnectDB().getConnection(); PreparedStatement ps = con.prepareStatement("SELECT * FROM student WHERE email = ? AND password = ?")) {
            ps.setString(1, email);
            ps.setString(2, password);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    studentNumber = rs.getInt("student_no");
                    return true;
                } else {
                    return false;
                }
            }
        } catch (SQLException e) {
            //e.printStackTrace();
            return false;
        }
    }

    public boolean verifyLoginTeacher(String email, String password) {
        try (java.sql.Connection con = new ConnectDB().getConnection(); PreparedStatement ps = con.prepareStatement("SELECT * FROM teacher WHERE email = ? AND password = ?")) {
            ps.setString(1, email);
            ps.setString(2, password);
            try (ResultSet rs = ps.executeQuery()) {
                return rs.next();
            }
        } catch (SQLException e) {
            //e.printStackTrace();
            return false;
        }

    }

